import numpy as np
rng=np.random.default_rng(3); N=2_000_000
def f(c,eps): return 0.5*(1+c)+eps*(c**3-c)

print("="*70)
print("БАТАРЕЯ 2: два ансамбля с ОДИНАКОВЫМ средним вектором оси")
print("="*70)
# Ансамбль A: ось +z с вер. 3/4, ось -z с вер. 1/4       -> среднее (0,0,1/2)
# Ансамбль B: 50/50 оси под ±60° к z в плоскости xz      -> среднее (0,0,1/2)
# Меряем вдоль z.
for eps in [0.0,0.2]:
    pA = 0.75*f(1.0,eps)+0.25*f(-1.0,eps)
    pB = f(0.5,eps)
    print(f"eps={eps}:  P(+z | A)={pA:.4f}   P(+z | B)={pB:.4f}   различимы: {abs(pA-pB)>1e-9}")

print()
print("="*70)
print("БАТАРЕЯ 3: несимметричная связь (частичная запутанность), q=0.85")
print("="*70)
# Экспериментальный факт: частично запутанные пары существуют.
# Для такой связи выбор оси Алисы рулит Боба в РАЗНЫЕ ансамбли с одинаковым средним:
#   Алиса мерит вдоль z: Боб -> {-z с вер. q, +z с вер. 1-q}        среднее (0,0,1-2q)
#   Алиса мерит вдоль x: Боб -> 50/50 { (±2sqrt(q(1-q)), 0, 1-2q) } среднее (0,0,1-2q)
# (оси единичные: 4q(1-q)+(1-2q)^2 = 1)
# Боб мерит вдоль z. Если статистика зависит от выбора Алисы -> сверхсветовой телеграф.
q=0.85; s=2*np.sqrt(q*(1-q)); zc=1-2*q
print(f"проверка единичности рулёных осей: {s**2+zc**2:.6f}")
for eps in [0.0,0.2]:
    # выбор Алисы: z
    axis_z = np.where(rng.random(N)<q,-1.0,1.0)          # z-компонента оси Боба: -1 или +1
    Bz = rng.random(N)<f(axis_z,eps)
    # выбор Алисы: x
    axis_x = np.full(N,zc)                                # z-компонента оси Боба всегда 1-2q
    Bx = rng.random(N)<f(axis_x,eps)
    diff=abs(np.mean(Bz)-np.mean(Bx))
    print(f"eps={eps}:  P(Боб +z | Алиса мерила z)={np.mean(Bz):.4f}   P(Боб +z | Алиса мерила x)={np.mean(Bx):.4f}"
          f"   -> сигнал: {'ДА, Δ=%.4f'%diff if diff>0.003 else 'нет'}")
print()
print("аналитика: Δ = eps*|(1-2q)^3-(1-2q)| =", f"{0.2*abs(zc**3-zc):.4f}")
